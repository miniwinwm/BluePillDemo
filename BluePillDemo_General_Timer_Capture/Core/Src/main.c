void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  static uint32_t timer_value;
  static uint32_t previous_timer_value;
  static uint32_t first_value = true;
  static uint32_t clock_divider = 4U;
  uint32_t difference;
  uint32_t clock_frequency;
  uint32_t pin_frequency;

  // check if this callback is for channel 1
  if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
  {
	// read the rimer value at capture
    timer_value = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

    // if first time round we don't have a previous value so don't do calculation
    if (!first_value)
    {
      // not first time round so have a previous value

      // work out difference between 2 subsequent captures; don't know which one will be bigger because of possible rollover
	  if (timer_value > previous_timer_value)
	  {
		difference = timer_value - previous_timer_value;
	  }
	  else
	  {
		difference = ((0xffff - previous_timer_value) + timer_value) + 1U;
	  }

	  // get the capture clock frequency and apply divider
	  clock_frequency = HAL_RCC_GetPCLK1Freq() / clock_divider;

	  // avoid division by zero
	  if (difference != 0U)
	  {
		// calculate the frequency on capture pin using clock frquenct and captured values difference
	    pin_frequency = clock_frequency / difference;
	    (void)pin_frequency;
	  }
    }

    // cache the latest capture value for next time
	previous_timer_value = timer_value;
    first_value = false;
  }
}

